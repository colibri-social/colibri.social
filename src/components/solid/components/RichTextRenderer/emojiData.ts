/** All emoji shortcode names, pre-sorted alphabetically for fast access. */
export const EMOJI_NAMES = Object.keys(EMOJI_DATA).sort();

/**
 * Search for emoji entries whose name starts with the given query string.
 * Returns at most `limit` results (default 24).
 */
export function searchEmoji(
  query: string,
  limit = 24,
): Array<{ name: string; emoji: string }> {
  if (!query) {
    // Return a curated "popular" set when no query is given
    const popular = [
      "thumbsup", "thumbsdown", "joy", "heart", "fire", "sparkles",
      "tada", "rocket", "eyes", "100", "wave", "clap", "pray", "sob",
      "thinking", "ok_hand", "muscle", "star", "slightly_smiling_face",
      "wink", "heart_eyes", "rofl", "sunglasses", "poop",
    ];
    return popular
      .filter((n) => EMOJI_DATA[n])
      .slice(0, limit)
      .map((name) => ({ name, emoji: EMOJI_DATA[name] }));
  }

  const q = query.toLowerCase();
  const startsWith: Array<{ name: string; emoji: string }> = [];
  const contains: Array<{ name: string; emoji: string }> = [];

  for (const name of EMOJI_NAMES) {
    if (startsWith.length + contains.length >= limit * 2) break;
    if (name.startsWith(q)) {
      startsWith.push({ name, emoji: EMOJI_DATA[name] });
    } else if (name.includes(q)) {
      contains.push({ name, emoji: EMOJI_DATA[name] });
    }
  }

  return [...startsWith, ...contains].slice(0, limit);
}
